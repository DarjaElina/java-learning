package Lesson1;

public class Lesson1 {

    // this is the main method
    // main is a starting point to execute any java program
    // without main we cannot run java program
    public static void main(String[] args) {
        // everything inside this curly braces will be executed
        System.out.println("Welcome to the first lesson!");
        System.out.println("Hello Java!");

        System.out.print("My name is ");
        System.out.println("Daria");

        System.out.print("A ");
        System.out.println("a");

        System.out.print("B ");
        System.out.println("b");

        System.out.print("C ");
        System.out.println("c");

        System.out.println("abcdefg");
        System.out.println();
        System.out.println("hijklmn");
    }
}