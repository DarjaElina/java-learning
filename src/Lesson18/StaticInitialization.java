package Lesson18;

public class StaticInitialization {
    public static void main(String[] args) {
        String[] array = new String[3];
        // Static initialization
        array[0] = "Hello";
        array[1] = "World";
        array[2] = "!";

        double[] array1;
        array1 = new double[2];
        array1[0] = 2.5;
        array1[1] = 3.5;
    }
}
